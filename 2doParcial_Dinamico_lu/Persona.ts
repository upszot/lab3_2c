class Persona
{
    nombre: string;
    apellido: string;
    mail: string;
    edad: number;
    sexo: string;

    constructor(nombre, apellido, mail, sexo, edad) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.mail = mail;   
        this.edad = edad;   
        this.sexo = sexo;
    }
    
}